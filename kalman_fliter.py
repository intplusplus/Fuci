class Kalman:
    x = 0.0  # 系统的状态量
    A = 0.0  # x(n)=A*x(n-1)+u(n),u(n)~N(0,q)
    H = 0.0  # z(n)=H*x(n)+w(n),w(n)~N(0,r)
    q = 0.0  # 预测过程噪声协方差
    r = 0.0  # 测量过程噪声协方差
    p = 0.0  # 估计误差协方差
    kgain = 0.0  # 卡尔曼增益

    def __init__(self, x, A, H, q, r, p):
        self.x = x
        self.A = A
        self.H = H
        self.q = q
        self.r = r
        self.p = p

    def kalman_fliter(self, measure):

        # predict
        self.x = self.A * self.x
        self.p = self.A ** 2 * self.p + self.q  # p(n|n-1)=A^2*p(n-1|n-1)+q

        # measurement
        self.kgain = self.p * self.H / (self.H ** 2 * self.p + self.r)
        self.x = self.x + self.kgain * (measure - self.H * self.x)
        self.p = (1 - self.kgain * self.H) * self.p

        return self.x

